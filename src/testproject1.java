public class testproject1 {


        ForWhileDoLoopExample() {
        }

        public static void main(String[] args) {
            int j;
            for(j = 0; j < 10; ++j) {
                System.out.println(j);
            }

            for(j = 0; j < 10; ++j) {
                System.out.println(j);
            }

            int k = 0;

            do {
                System.out.println(k);
                ++k;
            } while(k < 4);

        }
    }

